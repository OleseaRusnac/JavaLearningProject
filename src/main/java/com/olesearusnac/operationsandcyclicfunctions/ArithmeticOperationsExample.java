package com.olesearusnac.operationsandcyclicfunctions;

public class ArithmeticOperationsExample {
    public static void main(String[] args) {
        int a = 37;
        int c = 4;

        //Sum
        int sum = a + c;
        System.out.println("\nSum: " + sum);
        System.out.println("Sum: " + (a + c));

        //Subtraction
        int sub = a - c;
        System.out.println("The result: " + sub);

        //Multiplication
        int result = a * c;
        System.out.println("Multiplication: " + result);

        //Division
        result = a / c;
        System.out.println("The division: " + result);

        //Division with reminder;
        result = a % c;
        System.out.println("Division with reminder: " + result);
    }
}
